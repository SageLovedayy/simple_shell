/**
 * is_delim - check if delimiter
 * @c: add descr
 * @delim: add descr
 * Return: 1 if true, 0 if false
 */
int is_delim(char c, char *delim)
{
	while (*delim)
		if (*delim++ == c)
			return (1);
	return (0);
}
